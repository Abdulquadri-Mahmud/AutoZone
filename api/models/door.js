import mongoose from "mongoose";

var doorSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    make: {
        type: String,
        required: true
    },
    year: {
        type: Number,
        required: true
    },
    category:{
        type: String,
        required: true
    },
    descriptions:{
        type: String,
        required: true
    },
    price:{
        type: Number,
        required: true
    },
    deal:{
        type: String,
        required: true
    },
    DoorImage:{
        type: [], 
        required: true
    },
}, {timestamps: true});

const Door = mongoose.model('Door', doorSchema);

export default Door;